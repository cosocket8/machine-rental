// Helper: get the products string for a given machine type name (used on the detail page)
export function getProductsForType(machineType) {
  for (const category of Object.values(MACHINE_CATEGORIES)) {
    const found = category.find(m => m.name === machineType)
    if (found) return found.products
  }
  return null
}